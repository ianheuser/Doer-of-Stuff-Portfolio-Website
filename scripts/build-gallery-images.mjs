/**
 * Converts every project image listed in design-assets-originals/manifest.json
 * to webp under static/images/projects/gallery/<folder>/, and regenerates
 * src/lib/data/project-images.js (an id -> gallery-paths map consumed by
 * projects-all.js).
 *
 * Run with: node scripts/build-gallery-images.mjs
 */
import { readFile, writeFile, mkdir } from 'node:fs/promises';
import { existsSync } from 'node:fs';
import path from 'node:path';
import sharp from 'sharp';

const root = path.resolve(import.meta.dirname, '..');
const originals = path.join(root, 'design-assets-originals');
const outRoot = path.join(root, 'static/images/projects/gallery');

const MAX_WIDTH = 1600;
const QUALITY = 80;

const manifest = JSON.parse(await readFile(path.join(originals, 'manifest.json'), 'utf8'));

/** @type {Record<string, string[]>} */
const map = {};

for (const project of manifest) {
	const srcDir = path.join(originals, project.folder);
	const outDir = path.join(outRoot, project.folder);
	await mkdir(outDir, { recursive: true });

	map[project.folder] = [];

	for (const [i, image] of project.images.entries()) {
		const src = path.join(srcDir, image);
		const name = `${String(i + 1).padStart(2, '0')}.webp`;
		const dest = path.join(outDir, name);

		if (!existsSync(src)) {
			console.warn(`missing: ${project.folder}/${image}`);
			continue;
		}
		if (!existsSync(dest)) {
			await sharp(src)
				.resize({ width: MAX_WIDTH, withoutEnlargement: true })
				.webp({ quality: QUALITY })
				.toFile(dest);
		}
		map[project.folder].push(`/images/projects/gallery/${project.folder}/${name}`);
	}
	console.log(`${project.folder}: ${map[project.folder].length} image(s)`);
}

const banner = `/**
 * GENERATED by scripts/build-gallery-images.mjs — do not edit by hand.
 * Maps project id -> every gallery image for that project.
 */
export const projectImages = `;

await writeFile(
	path.join(root, 'src/lib/data/project-images.js'),
	banner + JSON.stringify(map, null, '\t') + ';\n'
);

console.log('\nwrote src/lib/data/project-images.js');
